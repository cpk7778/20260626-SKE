/** htxcrId별로 묶은 시계열 뷰 (sparkline·scatter에서 사용) */
export interface HexUnitSeries {
  htxcrId: string;
  cfId: HexStreamKey;
  dates: string[];  // bsDt 정렬순
  q: number[];
  ua: number[];
  uc: number[];
}

function bsDtToIso(bsDt: string): string {
  return `${bsDt.slice(0, 4)}-${bsDt.slice(4, 6)}-${bsDt.slice(6, 8)}`;
}

/** 'YYYYMMDD' 형식의 고유 날짜 목록 (정렬) */
export const HEX_DATES: string[] = Array.from(new Set(HEX_DATA.map(u => u.bsDt))).sort();

/** ISO 날짜 문자열로 변환한 날짜 목록 */
export const HEX_ISO_DATES: string[] = HEX_DATES.map(bsDtToIso);

/** cfId 스트림의 htxcrId 그룹 배열 반환 */
export function groupHexDataByStream(cfId: HexStreamKey): HexUnitSeries[] {
  const map = new Map<string, HexUnit[]>();
  for (const row of HEX_DATA) {
    if (row.cfId !== cfId) continue;
    if (!map.has(row.htxcrId)) map.set(row.htxcrId, []);
    map.get(row.htxcrId)!.push(row);
  }
  return Array.from(map.entries()).map(([htxcrId, rows]) => {
    rows.sort((a, b) => a.bsDt.localeCompare(b.bsDt));
    return {
      htxcrId,
      cfId,
      dates: rows.map(r => r.bsDt),
      q: rows.map(r => r.q),
      ua: rows.map(r => r.ua),
      uc: rows.map(r => r.uc),
    };
  });
}

/** 전체 HEX_DATA를 HexUnitSeries 배열로 변환 (HEX_STREAM_GROUPS 순서 유지) */
export function getAllHexUnitSeries(): HexUnitSeries[] {
  const map = new Map<string, HexUnit[]>();
  for (const row of HEX_DATA) {
    const key = `${row.cfId}::${row.htxcrId}`;
    if (!map.has(key)) map.set(key, []);
    map.get(key)!.push(row);
  }
  return Array.from(map.values()).map(rows => {
    rows.sort((a, b) => a.bsDt.localeCompare(b.bsDt));
    const first = rows[0];
    return {
      htxcrId: first.htxcrId,
      cfId: first.cfId,
      dates: rows.map(r => r.bsDt),
      q: rows.map(r => r.q),
      ua: rows.map(r => r.ua),
      uc: rows.map(r => r.uc),
    };
  });
}

